-- =============================================
-- Create Staging Buffer Distribution Function
-- =============================================
CREATE OR REPLACE FUNCTION distribute_staging_data()
RETURNS TRIGGER AS $$
BEGIN
    -- Step A: Upsert into Users
    INSERT INTO Users (
        user_id, uin, uin_hash,
        username, email, phone, date_of_birth,
        address, occupation, annual_income, kyc_status,
        kyc_verified_at, signature_hash, credit_score,
        blacklisted, blacklisted_at,
        current_rps_not, current_rps_360,
        risk_category, version,
        created_at, last_rps_calculation, time, diff, updated_at
    )
    VALUES (
        NEW.user_id, 
        NEW.uin, NEW.uin_hash,
        NEW.username, NEW.email, NEW.phone, NEW.date_of_birth,
        NEW.address, NEW.occupation, NEW.annual_income, NEW.kyc_status,
        NEW.kyc_verified_at, NEW.signature_hash, NEW.credit_score,
        COALESCE(NEW.blacklisted, FALSE), NEW.blacklisted_at,
        NEW.current_rps_not, NEW.current_rps_360,
        NEW.risk_category, COALESCE(NEW.version, 1),
        COALESCE(NEW.created_at, CURRENT_TIMESTAMP), 
        COALESCE(NEW.created_at, CURRENT_TIMESTAMP), 
        NEW.time, NEW.diff,
        CURRENT_TIMESTAMP
    )
    ON CONFLICT (user_id) DO UPDATE SET
        username = COALESCE(NEW.username, Users.username),
        email = COALESCE(NEW.email, Users.email),
        phone = COALESCE(NEW.phone, Users.phone),
        date_of_birth = COALESCE(NEW.date_of_birth, Users.date_of_birth),
        address = COALESCE(NEW.address, Users.address),
        occupation = COALESCE(NEW.occupation, Users.occupation),
        annual_income = COALESCE(NEW.annual_income, Users.annual_income),
        kyc_status = COALESCE(NEW.kyc_status, Users.kyc_status),
        kyc_verified_at = COALESCE(NEW.kyc_verified_at, Users.kyc_verified_at),
        signature_hash = COALESCE(NEW.signature_hash, Users.signature_hash),
        credit_score = COALESCE(NEW.credit_score, Users.credit_score),
        blacklisted = COALESCE(NEW.blacklisted, Users.blacklisted),
        blacklisted_at = COALESCE(NEW.blacklisted_at, Users.blacklisted_at),
        current_rps_not = COALESCE(NEW.current_rps_not, Users.current_rps_not),
        current_rps_360 = COALESCE(NEW.current_rps_360, Users.current_rps_360),
        risk_category = COALESCE(NEW.risk_category, Users.risk_category),
        last_rps_calculation = COALESCE(NEW.created_at, Users.last_rps_calculation),
        updated_at = CURRENT_TIMESTAMP,
        version = Users.version + 1,
        time = COALESCE(NEW.time, Users.time),
        diff = COALESCE(NEW.diff, Users.diff);

    -- Step B: Insert into ToxicityHistory
    INSERT INTO ToxicityHistory (
        user_id, 
        rps_not, rps_360, sanction_score,   
        news_score, transaction_score, portfolio_score,
        calculation_trigger,
        calculated_at, time, diff
    )
    VALUES (
        NEW.user_id, 
        NEW.rps_not, NEW.rps_360, NEW.sanction_score, 
        NEW.news_score, NEW.transaction_score, NEW.portfolio_score,
        NEW.calculation_trigger,
        COALESCE(NEW.created_at, CURRENT_TIMESTAMP), NEW.time, NEW.diff
    );

    -- Step C: Insert into UserSanctionMatches
    INSERT INTO UserSanctionMatches (
        user_id, match_found, match_confidence, matched_entity_name, checked_at, time, diff
    )
    VALUES (
        NEW.user_id, NEW.match_found, NEW.match_confidence, NEW.matched_entity_name, 
        COALESCE(NEW.created_at, CURRENT_TIMESTAMP), NEW.time, NEW.diff
    );

    RETURN NEW;
END;
$$ LANGUAGE plpgsql;

-- Create trigger on Staging_Buffer
DROP TRIGGER IF EXISTS trigger_process_staging ON Staging_Buffer;
CREATE TRIGGER trigger_process_staging
AFTER INSERT ON Staging_Buffer
FOR EACH ROW
EXECUTE PROCEDURE distribute_staging_data();

