CREATE TABLE ToxicityHistory (
    history_id BIGINT PRIMARY KEY GENERATED BY DEFAULT AS IDENTITY,
    user_id BIGINT NOT NULL,
    
    -- Risk Scores
    rps_not DOUBLE PRECISION,
    rps_360 DOUBLE PRECISION,
    sanction_score DOUBLE PRECISION,
    news_score DOUBLE PRECISION,
    transaction_score DOUBLE PRECISION,
    portfolio_score DOUBLE PRECISION,
    
    -- Metadata
    calculated_at TIMESTAMP WITHOUT TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    calculation_trigger VARCHAR(50),
    time BIGINT, -- Pathway timestamp field
    diff INT,    -- Pathway diff field
    
    -- Constraints
    CONSTRAINT chk_toxicity_rps_not CHECK (rps_not IS NULL OR (rps_not >= 0 AND rps_not <= 1)),
    CONSTRAINT chk_toxicity_rps_360 CHECK (rps_360 IS NULL OR (rps_360 >= 0 AND rps_360 <= 1)),
    
    FOREIGN KEY (user_id) REFERENCES Users(user_id) ON DELETE CASCADE
);

-- Create index for efficient user timeline queries
CREATE INDEX idx_user_timeline ON ToxicityHistory (user_id, calculated_at DESC);

