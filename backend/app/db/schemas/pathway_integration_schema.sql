-- =============================================
-- Create Users Table
-- =============================================
CREATE TABLE Users (
    user_id BIGINT PRIMARY KEY,
    uin CHAR(20),
    uin_hash CHAR(64),
    username VARCHAR(100),
    email VARCHAR(255),
    phone VARCHAR(15),
    date_of_birth TIMESTAMP,
    address TEXT,
    occupation VARCHAR(200),
    annual_income DOUBLE PRECISION,
    
    -- KYC Fields
    kyc_status VARCHAR(100),
    kyc_verified_at TIMESTAMP NULL,
    signature_hash VARCHAR(64),
    
    -- Credit & Risk Scoring
    credit_score INT,
    blacklisted BOOLEAN DEFAULT FALSE,
    blacklisted_at TIMESTAMP NULL,
    current_rps_not DOUBLE PRECISION,
    current_rps_360 DOUBLE PRECISION,
    last_rps_calculation TIMESTAMP,
    risk_category VARCHAR(100), 
    
    -- Version Control & Pathway Fields
    version INT DEFAULT 1,
    created_at TIMESTAMP WITHOUT TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    time BIGINT, -- Pathway timestamp field
    diff INT,    -- Pathway diff field
    updated_at TIMESTAMP WITHOUT TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    
    -- Constraints
    CONSTRAINT chk_age CHECK (
        EXTRACT(YEAR FROM AGE(date_of_birth)) >= 18
    ), 
    CONSTRAINT chk_credit_score CHECK (credit_score BETWEEN 300 AND 900),
    CONSTRAINT chk_current_rps_not CHECK (current_rps_not IS NULL OR (current_rps_not >= 0 AND current_rps_not <= 1)),
    CONSTRAINT chk_current_rps_360 CHECK (current_rps_360 IS NULL OR (current_rps_360 >= 0 AND current_rps_360 <= 1))
);

